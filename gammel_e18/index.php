<h1>Forlatt gammel del av E18</h1>
<p>Denne delen av E18 har blitt forlatt og stengt etter at ny motorvei har erstattet den.
Veien går gjennom Dørdal og ligger ikke langt unna E18 Kryss 53 Langrønningkrysset.
På et punkt går veien i bro over en grussti som heter Gongveien.
